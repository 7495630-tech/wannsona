// =============================
// 検索関数
// =============================
List<Breed> searchBreeds(String query) {
  if (query.isEmpty) return allBreeds;
  final q = query.toLowerCase();
  return allBreeds.where((b) {
    return b.jaName.contains(query) ||
        b.kana.contains(q) ||
        b.enName.toLowerCase().contains(q) ||
        b.aliases.any((a) => a.toLowerCase().contains(q));
  }).toList();
}

// =============================
// ミックス犬リスク合成
// =============================
BreedRisk mixBreedRisk(Breed b1, Breed b2, double weight1) {
  final w2 = 1.0 - weight1;
  int blend(int v1, int v2) => (v1 * weight1 + v2 * w2).round();
  int safe(int v1, int v2) => v1 > v2 ? v1 : v2; // 安全系はmax

  return BreedRisk(
    heatRisk: safe(b1.risk.heatRisk, b2.risk.heatRisk),
    coldRisk: blend(b1.risk.coldRisk, b2.risk.coldRisk),
    humidityRisk: safe(b1.risk.humidityRisk, b2.risk.humidityRisk),
    respiratoryRisk: safe(b1.risk.respiratoryRisk, b2.risk.respiratoryRisk),
    skinRisk: blend(b1.risk.skinRisk, b2.risk.skinRisk),
    jointRisk: safe(b1.risk.jointRisk, b2.risk.jointRisk),
    earRisk: blend(b1.risk.earRisk, b2.risk.earRisk),
    eyeRisk: blend(b1.risk.eyeRisk, b2.risk.eyeRisk),
    stomachRisk: safe(b1.risk.stomachRisk, b2.risk.stomachRisk),
    shortNose: b1.risk.shortNose || b2.risk.shortNose,
    longBack: b1.risk.longBack || b2.risk.longBack,
    giantBreed: b1.risk.giantBreed || b2.risk.giantBreed,
  );
}
