class BreedRisk {
  final int heatRisk;        // 暑さリスク 1-5
  final int coldRisk;        // 寒さリスク 1-5
  final int humidityRisk;    // 湿度リスク 1-5
  final int respiratoryRisk; // 呼吸器リスク 1-5
  final int skinRisk;        // 皮膚リスク 1-5
  final int jointRisk;       // 関節リスク 1-5
  final int earRisk;         // 耳リスク 1-5
  final int eyeRisk;         // 目リスク 1-5
  final int stomachRisk;     // 胃リスク 1-5
  final bool shortNose;      // 短頭種
  final bool longBack;       // 胴長
  final bool giantBreed;     // 超大型

  const BreedRisk({
    this.heatRisk = 3,
    this.coldRisk = 3,
    this.humidityRisk = 3,
    this.respiratoryRisk = 3,
    this.skinRisk = 3,
    this.jointRisk = 3,
    this.earRisk = 3,
    this.eyeRisk = 3,
    this.stomachRisk = 2,
    this.shortNose = false,
    this.longBack = false,
    this.giantBreed = false,
  });
}

class Breed {
  final String id;
  final String jaName;
  final String kana;
  final String enName;
  final List<String> aliases;
  final String jkcGroup;
  final String akcGroup;
  final String sizeDefault; // SS/S/M/L/XL
  final String coatLength;  // 短毛/中毛/長毛
  final String coatType;    // シングル/ダブル/巻き毛/ワイヤー
  final String exerciseLevel; // low/medium/high/very_high
  final BreedRisk risk;
  final String notesJa;

  const Breed({
    required this.id,
    required this.jaName,
    required this.kana,
    required this.enName,
    this.aliases = const [],
    this.jkcGroup = '',
    this.akcGroup = '',
    required this.sizeDefault,
    required this.coatLength,
    required this.coatType,
    this.exerciseLevel = 'medium',
    required this.risk,
    this.notesJa = '',
  });
}

